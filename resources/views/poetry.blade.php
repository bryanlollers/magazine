            <h4  class="active">Workshop of a Rape Story </h4>
            <h6  class="active"> Fiction | Lucy Marcus </h6>
            <br>
            <p>
                Today's writer feels the cone in all its silent suffocation. She wants to take it off.
                It doesn't quite fit, that's the problem. Its circumference squeezes her forehead,
                producing an aching, yellow pressure at her temples. The little straps pinch the loose skin
                below her chin, where an Adam's apple might protrude if she were a man
            </p>
            <p>
                She and the other eight students wait in the quiet for their professor, who is always a 
                little bit late. In front of each students sits a copy today's story. covered in black and
                blue pen marks.
            </p>
            <p>
                It's a classroom policy, the wearing of this cone-no speaking as your peers and professor
                examine your story. studying its triumps and pitfalls under the surgical microscopes of 
                thick-rimmed bifocals. Without the cone, one might feel the burdening impulse to defend,
                define, and declare. It's necessary, this cone, and each student has taken a turn wearing it
            </p>
            <p>
                Today's writer forgets that fact as she glances around the table. She admires with some envy
                as the smallest, loveliest baby hairs wave over her peers' unblemished foreheads in the breeze
                of the overhead fan. She avoids their eyes, feeling, herself, quite unattractive. The cone
                looks so silly, like a dunce's cap, not even a supermodel could pull it off. This writer is no supermodel

            </p>
            <p>
                At six minutes after the hour, the professor blows through the door, dumping her belongings on
                the rounded wooden table, removing her silk-lined pea coat, collapsing into a chair. The professor
                is a young woman, not much older than the writer. Her pacific publications are displayed on
                special shelves in almost every feminist bookstore across the country.
            </p>
            <p>
                "Let's get started," she says in a voice made hoarse by cigarettes and long periods of diuse.
                Every workshop begins with the things that are working. The rule is that you must be specific
                with your praise. You must define it with an exact-o-knife.
            </p>
            <p>
                When the students speak, they look the professor in the eyes, reading her expression. 
                "I like the imagery, especially the dazed walking part," one student says. "The sensory
                details were really vivid."
            </p>
            <p>
                "The setting was well-established," another adds, flipping through her pile of pages,
                searching for an example. "Like here! Page five," she speaks over the collective shuffling
                of pages. "We get a full paragraph on the weather. This writer doesn't usually give us weather 
                and I really appreciated it in this piece."
            </p>
            <p>
                The other nod and mutter agreement.
            </p>
            <p>
                "I was a little confused by tone," a pigtailed student states. She has written TONE over and
                over on the margins in letters big enough for today's writer to see through her sideways
                glimpse. "I get a lot of intense emotion on the first page, but then it kind of loses momentum.
                 And we end with this strange joke. Or did I miss something? Maybe I missed something." She looks
                 around with her shoulders and her ears, then relaxes as the professor nods, lifting her
                 fresh piece of chalk from the table. 
            </p>
            <p>
                "I was hoping someone would bring up tone," the professor says with her back facing the class as
                she draws on the blackboard. She likes to insert lessons into workshops, waiting for the right
                issues to come up in the students' writing. She draws a mathematical-looking diagram on the board,
                with audibly marked xs and dashes.
            </p>
            <p>
                The student writer's cone-induced pressure headache obscures the meaning of this lesson. Still, she 
                scribbles a poor copy of the diagram into her notebook.
            </p>
            <p>
                "I also wanted a little more clarity around the narrator's appearance," another student shares after
                the professor sits again. "What was she wearing? How many drinks did she consume, and did she chug them
                 or sip them with a pinky in the air? Those kind of details could make this so much more real."
            </p>
            <p>
                The other nod. Now that the first critiques have been shared, the others pour out like a collapsed
                dam. They want more purposeful details,
            </p>
            <p>
                Too many details, unnecessary dialogue, the writer scribbles in her notebook. Fuzzy timeline. Fix
                 tense shifts.
            </p>
            <p>
                "I'm concerned about the stereotypes," a student with quill pen shares. He sounds very serious, like 
                he's been practicing this comment in his head. "In this political climate, we've got to be really 
                careful about which stereotypes we fulfill and which we disrupt, you know? To have a Latino man 
                commit this crime, and in Mexico City on top of that, I guess I wonder if it can happen in a place that 
                might be more surprising."
            </p>
            <p>
                "Though the man is a white Latino-American man," another student corrects. "I mean, the racial 
                dynamics aren't exactly problematic in that way. Though I do agree that the gender roles are pretty 
                clinched. It could definitely be more original."
            </p>
            <p>
                "Right, it's a little cliched, that's all," the quill pen continues. He writes something with wet ink
                into his private, leather-bound journal.
            </p>
            <p>
                This is where the cone comes handy. The writer cannot blabber, "But that's how it happened!" She 
                cannot define, "And he's Cuban American, not Mexican." She cannot be defend, "But I don't 
                remember." She cannot cry, either, or release the strange laughter she has a new habit of releasing
                in such moments of recollection.
            </p>
            <p>
                Instead, she continues to take notes in her notebook. She transcribes every word, to be 
                dissected for value later, when she is alone.
            </p>
            <p>
                The workshops always end the same. The professor exhales, pushing a shiny chunk of brown
                hair behind her ear, and looks at the student writer's face for the first time. The 
                professor's glance is flirtatiously embarassed, as though recognizing her partner after an
                 extravagrant bout of lovemaking. She smiles, radiant, with all her whitened teeth. "Does our writer 
                 today have any questions?" This time the term "writer" is coy, entirely aware of audacity.
            </p>
            <p>
                The writer gulps down a stale pond of spit in her mouth and opens her lips. When she tries
                to speak, she finds an empty well where sound might've been gathered. The cone seems to be doing
                its work. She shakes her head and makes an effortful smile as her peers and over their copies
                with apologetic looks that don't quite meet her eyes.
            </p>
            <p>
                She stuffs the jagged pages into her bag and, wanting a swift escape, hurries to the door while 
                zipping it. Before she can make it through, her head jolts backwards, followed by her body.
                She touches her cone-elongated head and laughs as she balances herself. She turns to see the other
                students, who pause their converstations to stare.
            </p>
            <p>
                She knows she can take off the cone right there and leave it in the classroom for the next writer
                wear, but she feels its tautness on her forehead. It is sealed with sweat to her skin. With 
                the cone, she has three extra feet, extending synthetically skyward. As she traces her pointer 
                finger over the material on her forehead, she feels a surrendering comfort. She moves through the 
                doorway again, this time ducking in order to fit.
            </p>
            <p>
                All the way home, the writer wears the cone, crouching through doors, avoiding stares, making
                no sounds at all.
            </p>

