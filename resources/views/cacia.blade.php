                How do you define yourself as an artist? 
                You wear a number of hats—you're a model, a videographer, a director, a photographer... 
                but what is your favorite medium to work with?
            </p>
            <p>
                I know I can be a maniac. I so want to do the best I can with everything I love, and I love art, creativity, and expression. 
                There is not one moment that goes by that I don't think about art and ideas. I also am just too stubborn to choose and too fickle to settle on one medium.
                 I have to always be doing and making. I never want to restrict myself to one field, so I need to also work three times as hard to get anything done. 
                 As long as everything I do is creativity related, my practices and skill all compensate and enhance one another. My dance background helps me be a good model,
                  for I can always project myself from a photographer’s angle. My photographer’s composition mindset helps me see how I want to move from one scene to the next 
                 video. Painting helps me actualize concept and see bigger pictures. Music moves me, transcends my mind and paints a story—non-lyrical music, that is.  
                 At any time of the day, I’m always developing ideas. I will say I increasingly love video as a medium, compared to photography, which it is a still image that
                 can be interpreted in million ways. Video also incorporates a process, a timeline, a beginning, middle, and end, a story also with sound and audio. 
                 I think video is the closest medium of art that can [really convey my ideas]. . . if not completely, it’s close to that. Still images sometimes need words to
                  compensate to the image and its meaning; in video I can put all my emotion, intention, and creativity [into them].


