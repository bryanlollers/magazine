                <h2 class="active">GENERAL RULES FOR SUBMISSION (INCLUDING RIGHTS AND PAYMENT)</h2>
                <ul>
                    <li>
                        We will only accept submissions that have not been previously published electronically and/or on the internet.
                         New Reader Magazine purchases First Electronic Rights for submissions to our quarterly journal as well as our website. 
