            <h4  class="active">Artist Profile: Jan Sunday </h4>
            <h6  class="active"> Artist Profiles | February 2018 | by M. Dominique Dela Paz </h6>
            <br>
            <p>
                Jan Sunday is a conundrum. On the one hand, as the lead vocalist of punk band <b>Tiger Pussy,</b> she plays an agent of chaos and disorder—and she plays it
                 well.
            </p>
            <p>
                On the other hand, as a visual artist, Jan Sunday has a thoughtful, even quiet sensibility. Not an easy, placid kind of quiet, but the uneasy silence that 
                comes with contemplation and introspection that can and frequently does turn dark.
            </p>
            <p>
                She tells me about the subject of one of her photo sets: “This woman, well, she didn't quite know yet what she wanted until she gave up trying.
                 The moment she became honest with herself, it got dark and ugly . . . but she was set free.”
            </p>
            <p>
                The sacred, mysterious feminine is a consistent theme in Jan Sunday’s work. “Having issues of my own with my body, self image and sexuality, 
                I've chosen to glorify and find a sense of beauty with the feminine form from my perspective; In the darkness. The confusion, the pain, the mystery of
                 womanhood.”

            </p>
           <img src="/images/jan-sunday.jpg" style="width:100%;"><br><br>
            <p>
                When asked to speak about herself, Jan Sunday says, “I can't think of anything artsy or witty to say. I actually find myself boring. I do graphic design and work
                 from home. I barely get to go out except for dates or gigs or when I need to travel for an exhibit or an art event.
            </p>
            <p>
                “As a child, I always found myself making stuff that only I could understand. I'm an only child. Maybe it's a coping mechanism that I developed out of 
                loneliness and trauma. I guess this is how it is when you spend your whole life in your room, just thinking. You eventually find outlets just so you could 
                continue to function well in society.
            </p>
            <p>
                “But I started to take this seriously only around three or four years ago. Left my corporate job. Found me a more convenient setup so I could 
                pursue this—whatever this is.”
            </p>
            <p>
                She lists her interests as ancient civilizations, political theories, dystopian novels, the mind, time and space, existentialism, punk rock,
                 the sacred feminine, and spicy food. “I'm obsessed with the concept of time,” Jan Sunday says, “and to hold a moment is fascinating, and even more when 
                 you can manipulate it. It's like being a god.”
            </p>
            <p>
                Jan Sunday is inspired by a variety of artists, from Magritte and Frida Kahlo to Francis Bacon. But, she says, “with execution, it's always been organic.
                 The reference is the person. What I know and feel from them is how the image is going to look like. The same applies to my self portraits in photography 
                 and painting. A mirror. Sometimes ugly. Sometimes vague and abstract.”
            </p>
            <p>
                People’s stories inspire her. “And the theories, philosophies, and history of the feminine and women.”
            </p>
            <p>
                The photographer prefers monochrome shots—“It breaks the familiarities that colors give and emphasizes on light and forms. 
                It's as if there is more to wonder with simplicity”—but these days she’s branching out to installation art and beefing up her portfolio for a solo exhibit 
                potentially coming out this year.
            </p>
