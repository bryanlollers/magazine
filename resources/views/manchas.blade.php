                    <p>
                        But as with every good story, the most engaging thing about Don Mancha’s writing is not the world it is set in but the characters, 
                        all of them “based more on [his] interpretation of certain ideals.” Ryan, the main character, is a stoic, pink-haired teenager (?) with an alcoholic for
                        a roommate and an ornery, snarky old lady for a boss. One evening, Ryan’s humdrum existence in Purgatory is shaken up by the appearance of a little kid
                        in need of his help—a young (?!) girl who mysteriously knows his name and starts appearing everywhere, including in the pages of his magazine.
                     </p>
                    <br><br>
                 <p>
                        In chapter two, “Lucid,” we are introduced to another character, a street artist named Miles who died trying to 
                        complete a masterpiece that his brother also died trying to make. What follows is a touching story of family, passion, and the drive to create against all odds—all in 
                        the span of about a dozen panels. (Check it out <a href="https://www.purgatorythecomic.com/chapter-2-lucid.html" target="_blank">here.</a>)
                    </p>
