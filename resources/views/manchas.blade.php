            <h4  class="active">Artist Profile: Don Mancha’s Purgatory </h4>
            <h6  class="active"> Artist Profiles | December 2017 | by M. Dominique Dela Paz</h6>
            <br>
            <p>
                Are you tired of your life? Sick of your nine-to-five? Have you lived a middling, mediocre life and committed sins too venial for Hell and too gnarly for Heaven? 
                Then take a trip down to Purgatory, where the dead get another shot at life, or closure, redemption, or whatever it is they’re doing down there in the gritty,
                 deadpan universe of writer Don Mancha III.
            </p>
            <p>
                Purgatory is a webcomic written by Don Mancha III and illustrated by Anubisazp. According to the website’s About page, it is “the story of dead people dealing
                 with being dead while navigating a slowly decaying limbo.” And slowly decaying it is; two chapters in, purgatory’s delivery trucks have been stolen, meaning 
                 main character Ryan can’t refill his prescription of what we assume is antipsychotic medicine—though it could be anything, really, in Don Mancha’s world of 
                 car-throwing cats and giant toy robots.
            </p>
            <p>
                For those of us who were raised Roman Catholic, purgatory was the in-between place between heaven and hell, a place of fire and brimstone and purification. 
                Don Mancha’s version of purgatory is “more of the same;” it looks like our world, except reality seems to be a little bit more malleable here. 
                It was inspired, he says, by “San Francisco. Scott Pilgrim. Every employee at McDonald’s.
            </p>
            <p>
                “I wanted life’s apparent unfairness to be front and center. After dying, instead of a blissful peace or a horrible, endless torture, purgatory is 
                just more of the same. For somebody who feels they've earned that peace they might feel cheated. For somebody who was afraid of an endless pain, this 
                is a godsend. That’s something I want to hit hard as Purgatory goes on.”

            </p>
            <div class="row">
                <div class="col-md-6">
