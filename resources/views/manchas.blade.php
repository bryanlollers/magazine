                  <p>
                   
                        Don Mancha tells us that he’s very much influenced by Scott Pilgrim (“the best graphic novel series ever. Fight me.”) and the Gorillaz 
                        (“the best band ever. Fight me”). And “JoJo’s Bizarre Adventure,” the best-selling manga series in history, “is gonna sneak its way in at some point.”
                  </p>
                  <br><br>
                <div class="row">
                <div class="col-md-6">
                    <img src="/images/anubisazp-pic.png" style="width:100%;" ><br><center style="color:#c2c2c2"><i>Anubisazp</i></center>
                </div>
                <div class="col-md-6" style="margin-top:50px;">
                    <p>
                            As for the aptly named Anubisazp (who does have a real name but did not give it to us. The curious will have to ask him for themselves),
                             he says that inspiration is “pretty much everywhere. Movies I watch, games I play, dumb stuff that happens to me, and of course, insane dreams
                              I have all contribute. Two styles that inspired me the most, however, were those of ‘Samurai Jack’ and ‘Hellboy.’”
                     </p>
                 <p>
                        There is a dark, frenetic energy in Anubisazp’s <b>work</b> that grooves perfectly with Don’s matter-of-fact, acerbic 
                        <a href="https://donscontrolledchaos.weebly.com/" target="_blank">voice.</a>
                    </p>
                </div>
                <div class="clearfix"></div>
            </div>
            <br>
            <p>
                They found each other quite by accident—or, rather, Don found him during a random Google search and “tore through most of his gallery. 
                I stalked him on Facebook for a few days until I thought I should talk to him.
            </p>
            <p>
                Anubisazp is not the first artist to work with Don on Purgatory. Earlier iterations of the story—including one which, according to Don, was a total ripoff
                 of Scott Pilgrim—were illustrated by different artists. But those didn’t quite pan out
            </p>
            <p>
                I asked them what the process of collaborating on a project like this looks like. Is it difficult, considering Don’s in California and Anubis 
                is all the way in Serbia? Turns out it’s almost too simple. Anubis says, “Don usually announces that a task is ahead, then sends a script.
                I draw pages by hand and send him pencil works for review and edits. Then I ink digitally and send him finished works in zip file.
            </p>
            <p>
                “He describes key things that should be on an image, and what characters are doing and talking. The rest is up to me.”
            </p>
            <p>
               Did they have any words for aspiring writers and artists? 
            </p>
            
            <p>
                Anubis says, “I have two. First one being: don't expect to make a living off of your art. There is a chance over 99% that you won't, not at first anyway. 
                I need a regular job to pay the bills, on top of having 3 art jobs. Art is just unreliable way to earn your cash till you make a name for yourself and people
                 actually start coming to you. 
            </p>

             <p>
                 “Second one might sound unusual, so let me explain. Most of people that I know are artists. Only two of them are happy with their lives. 
                 It's not unusual that we say we'd like to kill ourselves and finally get the rest from everyday's problems. Thing is, some didn't joke when they said it.
            </p>

            <p>
                “Don't give up yet. Tomorrow you may feel better, and see things for what they are. Or simply, you might get contacted for an art job. Either way, you win.”
            </p>

            <p>
                And Don? 
            </p>

             <p>
                    “Yes. 
            </p>

            <p>
               “You’re horrible at whatever it is you’re trying to do, so get better.”
            </p>
